/******************************************************************************
*
* @brief	:
* @param in	:
* @param out:
* @return 	:
* @note		:
********************************************************************************/
UINT32 SORT_BinSearch(UINT32 v[], UINT32 n, UINT32 x)
{
	UINT32 low = 0, mid, high = n - 1;

	while(low <= high) 
	{
		mid = (low + high) / 2;

		if(x < v[mid])
		{
			high = mid - 1;
		}
		else if(x > v[mid])
		{
			low = mid +1;
		}
		else
			return mid;
	}

	return MARK_VALUE_INVALID;
}

/******************************************************************************
*
* @brief	:
* @param in	:
* @param out:
* @return 	:
* @note		:
********************************************************************************/
void SORT_Insert(UINT32 a[], UINT32 len)
{
	UINT32 i, j, key;

	for (j = 1; j < len; j++) 
	{
		key = a[j];
		i = j - 1;

		while (i >= 0 && a[i] > key) 
		{
			a[i + 1] = a[i];
			i--;
		}

		a[i + 1] = key;
	}
}

/******************************************************************************
*
* @brief	:
* @param in	:
* @param out:
* @return 	:
* @note		:
********************************************************************************/
void SORT_Bubble(UINT32 array[], UINT32 num)
{
	int i = 0;
	int j = 0;
	int tmp;
	
	for (i = 0; i < num; i++)
	{
		for (j = 1; j < num - i; j++)
		{
			if (array[j - 1] > array[j])
			{
				tmp = array[j];
				array[j] = array[j - 1];
				array[j - 1] = tmp;
			}
		}
	}

	return;
}

